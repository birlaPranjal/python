# Assignment Operators

# = (Simple Assignment)
x = 10
print("x =", x)  # Output: x = 10

# += (Add and Assign)
x += 5
print("x =", x)  # Output: x = 15

# -= (Subtract and Assign)
x -= 3
print("x =", x)  # Output: x = 12

# *= (Multiply and Assign)
x *= 2
print("x =", x)  # Output: x = 24

# /= (Divide and Assign)
x /= 4
print("x =", x)  # Output: x = 6.0

# //= (Floor Divide and Assign)
x //= 2
print("x =", x)  # Output: x = 3.0

# %= (Modulus and Assign)
x %= 2
print("x =", x)  # Output: x = 1.0

# **= (Exponentiate and Assign)
x **= 3
print("x =", x)  # Output: x = 1.0

# &= (Bitwise AND and Assign)
x = 5
x &= 3
print("x =", x)  # Output: x = 1

# |= (Bitwise OR and Assign)
x = 5
x |= 3
print("x =", x)  # Output: x = 7

# ^= (Bitwise XOR and Assign)
x = 5
x ^= 3
print("x =", x)  # Output: x = 6

# >>= (Right Shift and Assign)
x = 10
x >>= 2
print("x =", x)  # Output: x = 2

# <<= (Left Shift and Assign)
x = 10
x <<= 2
print("x =", x)  # Output: x = 40