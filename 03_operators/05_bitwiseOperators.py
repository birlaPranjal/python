# Bitwise AND
a = 5  # 0101 in binary
b = 3  # 0011 in binary
result = a & b  # 0001 in binary
print("Bitwise AND:", result)

# Bitwise OR
a = 5  # 0101 in binary
b = 3  # 0011 in binary
result = a | b  # 0111 in binary
print("Bitwise OR:", result)

# Bitwise XOR
a = 5  # 0101 in binary
b = 3  # 0011 in binary
result = a ^ b  # 0110 in binary
print("Bitwise XOR:", result)

# Bitwise NOT
a = 5  # 0101 in binary
result = ~a  # -6 in decimal
print("Bitwise NOT:", result)

# Bitwise Left Shift
a = 5  # 0101 in binary
result = a << 2  # 10100 in binary
print("Bitwise Left Shift:", result)

# Bitwise Right Shift
a = 5  # 0101 in binary
result = a >> 2  # 0001 in binary
print("Bitwise Right Shift:", result)