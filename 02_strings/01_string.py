# Single-line string
single_line_string = "Hello, World!"
print(single_line_string)

# Multiline string
multiline_string = '''This is a
multiline
string'''
print(multiline_string)

# Accessing string as an array
string = "Hello, World!"
print(string[0])    # Output: H
print(string[7])    # Output: W
print(string[-1])   # Output: !
print(string[-2])   # Output: d